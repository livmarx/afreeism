<p>
This all may seem confusing and disorienting.  However, once we become comfortable with this perspective it can feel very liberating.  Realizing that the world is deterministic and that we have no free will makes us look at some things very differently and other things quite the same.  In order to understand the afreeist perspective, we must keep in mind two things:
</p>
<p style="margin-left:20px;">
1.	The universe unfolds through a causal chain, and
</p>
<p style="margin-left:20px;">
2.	Causation looks quite different depending on whether we are looking to the future or looking at the past.
</p>
<p>
The lens of causation illustrates the dual nature of causality.  Imagine a landscape of time going forward into the future and backwards into the past.  You observe both the future and the past through a special lens, the lens of causation.  What do you see?  First, look forward into the future.  You cannot see the future with precision through this or any other lens.  Instead, roads spread out in front of you and go off into the distance.  These roads fork in a seemingly infinite pattern of possibilities leading to outcomes ranging from the sublime, to the beautiful, to the mundane, to the terrifying.  Sometimes you cannot see where a road is going, or you cannot see it clearly.  The road may disappear over a hill, for example.  But you can see many of the roads and forks.  Robert Frost poetically described such a fork in the road:
</p>
<p style="margin-left:20px;">
Two roads diverged in a yellow wood,<br />
And sorry I could not travel both<br />
And be one traveler, long I stood<br />
And looked down one as far as I could<br />
To where it bent in the undergrowth;<br />
Then took the other, as just as fair,<br />
And having perhaps the better claim,<br />
Because it was grassy and wanted wear;<br />
Though as for that the passing there<br />
Had worn them really about the same . . . 
</p>
<p>
Some of the forks in the roads represent choices that you must make.  In decision theory, these are called choice nodes.  Other forks represent paths that could turn out one way or the other unpredictably, so-called random events.  These forks are, in decision theory, chance nodes. 
</p>
<p>
Looking forward into the future through the lens of causation, you can see that what you do, what you choose, makes a difference.  You may not be able to predict exactly what will happen, but you can have some influence over it.  Taking different roads leads to different outcomes.  Due to causation, the decisions that you make will, along with other factors, determine the outcomes.
</p>
<p>
In a sense, looking at the future through the lens of causation looks a lot like what all of us do naturally.  We come to decision nodes and we try to predict the outcomes from making one decision or another.  Because we have the knowledge and understanding that events in the universe are caused, we can make predictions, although often imperfect ones.  If we know enough about the causes, we can make good predictions.  If we know less, then our predictions will be less ideal.  Nevertheless, it is clear that what we do matters.
</p>
<p>
Let us suppose now that you have come to a fork, to a choice node, and you must decide whether to take one road or another.   You look ahead and, using causation, try to determine how your choice will affect your life and the lives of others.  You weigh your options and make a choice.  You proceed down one of the roads.
</p>
<p>
Now turn around and look through the same lens of causation towards the past.  It looks quite different than you remember.  The beautiful pattern of possibility has disappeared.  All you can see is one road as far back as you can see.  It is the road that you have traveled.  You see no other roads.  You see no forks, no crossroads.  Because of causation, there was in reality only one road.
</p>
<p>
What does this mean?  First, looking ahead you see “possibilities.”  Because the world is causal, what you do matters.  What road you take will determine (along with a host of other factors), where you end up.  From your perspective, then, it is important to plan well, to research your options, and to make the best choices you can with the information you have.  As part of your decision making algorithm, you likely will take into account what you value in life, your feelings and emotions, your relationships with people around you, and your philosophical view of what is the good society, among other things.  (Of course, your algorithm is also determined.  Some people may not consider any of these things.)  Again, causation implies that what you do matters.  It affects how happy you will be and how happy those you care about will be and, in some small way perhaps, the type of world you and those who come after you will live in.  That is what causation says.  What you do causes a host of other events and actions stretching into the future.  You are a cause.  A caused cause.
</p>
<p>
However, once you have made a decision and have, so to speak, moved past that fork in the road, you will now be able to turn around look at that decision in the past through that same lens of causation.  All of the untaken options now have disappeared, and you realize that the action that you took was the only action possible.  This hindsight perspective is immensely freeing.  You cannot feel guilty, ashamed, remorseful or regretful, after all, for actions that simply had to be.
</p>

