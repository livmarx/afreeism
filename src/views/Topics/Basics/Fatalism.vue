    <div class="TopicHeading">Determinism versus Fatalism</div>
<p>
Some people believe that if the world is determined, and outcomes set in stone, then it is pointless to try to improve ourselves or to improve the world.  What is going to happen, will happen.  This misunderstands determinism and afreeism.  It is precisely because the world is causal that what we do makes a difference.  Afreeism is a philosophy of action, not of futility.  
</p>
<p>
One reason that people assume that determinism implies futility is that they confuse fatalism and determinism.  Fatalism says that a specific outcome is inevitable no matter what a person does.  To say that I am fated to die at the age of 43, for example, is to say that, no matter what I do, no matter how well I eat, no matter how much exercise I get, no matter how many or few life-threatening activities I engage in, I am fated to die at 43.   Fatalism says that outcome C will occur irrespective of whether I take action A or action B.
</p>
<p>
This is not determinism.  Because the universe is deterministic, it may be true that I will die at the age of 43, but I will do so because of actions that I take, as well as the result of other factors unrelated to my actions.  If I had gotten more exercise, eaten better, gone to the doctor more often, then I would not have died at the age of 43.  Unfortunately (for me), I could not have done otherwise than what I did.  The causal chain led inevitably to me getting less exercise, eating unhealthily, avoiding my doctor, etc.  Thus, although I am one of the causes of my own demise, I could not have done otherwise.   
</p>

